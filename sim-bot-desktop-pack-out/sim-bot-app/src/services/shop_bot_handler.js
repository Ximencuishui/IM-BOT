/**
 * 检测消息是否与行业（盲人按摩/理疗/美容店）相关
 */
export function isShopRelatedMessage(text) {
  const t = String(text || '').trim();
  if (!t) return false;
  const shopKeywords = [
    // 通用咨询
    '你好', '在吗', '请问', '有吗', '有没有', '多少钱', '怎么收费', '价格',
    // 位置
    '怎么去', '怎么到', '在哪里', '位置', '地址', '导航', '路线',
    // 服务
    '项目', '服务', '按摩', '理疗', '美容', '推拿', '拔罐', '刮痧', '艾灸',
    '足疗', '修脚', '采耳', 'spa', 'SPA',
    // 预约
    '预约', '预订', '订位', '几点', '时间', '今天有空', '明天有空',
    // 会员卡
    '会员卡', '会员', '次卡', '销卡', '充值', '余额', '还剩', '多少次',
    // 营业
    '营业', '开门', '几点关门', '几点开门', '电话', '联系方式',
    // 老板
    '老板', '老板娘',
  ];
  return shopKeywords.some((kw) => t.includes(kw));
}

